import { Schema } from "mongoose";

export const MenuItemSchema = new Schema({
    img: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true,
        trim: true
    },
    price: {
        type: Number,
        required: true,
        min: 0
    },
    description: {
        type: String,
        required: true,
        trim: true
    }
});
